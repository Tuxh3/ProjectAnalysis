    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int cantidadTesoros = Integer.parseInt(JtextEstrellas.getText());
        ArrayList<Tesoro> listaTesoros = new ArrayList<Tesoro>();
        for (int i = 0; i < cantidadTesoros; i++) {
            int fortunaTesoro = (int) Math.random() * 5 + 1;
            listaTesoros.add(new Tesoro(fortunaTesoro));
        }
        int cantidadEsclavos = Integer.parseInt(JtextEsclavos.getText());
        ArrayList<Esclavo> listaEsclavos = new ArrayList<Esclavo>();
        for (int i = 0; i < cantidadEsclavos; i++) {
            int edadEsclavo = (int) Math.random()*80 + 1;
            String edadEsclavo2 = "";
            if(edadEsclavo <= 30){
                edadEsclavo2 = "joven";
            }
            if(edadEsclavo > 30 && edadEsclavo <= 60){
                edadEsclavo2 = "adulto";
            }
            if(edadEsclavo > 60){
                edadEsclavo2 = "viejo";
            }
            listaEsclavos.add(new Esclavo(edadEsclavo2));
        }
        
        new Isla2(JtextNombreIsla.getText(), listaTesoros,
                JtextPirataJefe.getText(), listaEsclavos);
        
        frame1.setEnabled(true);
        this.setEnabled(false);
        this.setVisible(false);
        
    }//GEN-LAST:event_jButton1ActionPerformed

